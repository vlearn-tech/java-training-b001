package com.tech.javabasics.step07Operators;

public class AssignmentOperators {
	public static void main(String[] args) {
		int someNumber = 100; // assignment
		System.out.println("Some Number Value is : " + someNumber);

		someNumber += 10;
		System.out.println("Some Number Value is : " + someNumber);

		someNumber -= 15;
		System.out.println("Some Number Value is : " + someNumber);

		someNumber /= 23;
		System.out.println("Some Number Value is : " + someNumber);

		someNumber %= 2;
		System.out.println("Some Number Value is : " + someNumber);

	}
}
